            <div class="blog-pic"><img src="./images/blog-pic-1.jpg" width="598" height="178" /></div>
            <div class="blog-text">走四方客製鞋墊的類別，主要是以半客製或是全訂製的醫療極鞋墊為主力，一切以實用及效果為主要訴求，也因為這樣，無法兼顧所謂的外觀或多彩複合材料等噱頭（因為材料使用愈多，代表著損壞速度越快）。而鞋墊的優劣，有90%取決於製作及評估者的專業度，這也是主要的成本來源，請不要用一般市面上其它的鞋墊價格來議價，相信且尊重我們的專業，走四方會給您最好的服務。<br /><br />
                <img src="./images/blog-pic-3.jpg" width="547" height="204" />
                <br /><br />
                走四方不只有訂製鞋墊，還有足踝支架類的產品，我們以定期的專業訓練，提供直接在門市取模及維修的專業技術。
                <br /><br />
                讓忙碌的您不用再為支架沒處維修而煩惱。若您有這一方面的需求，歡迎與我們聯絡及預約取模。
                <br /><br />
                <img src="./images/blog-pic-2.jpg" width="598" height="151" />
                <br /><br />
                <span class="font-blue-14">製作流程</span>
                <br />
                步驟一：電話或是傳訊溝通所需要輔具種類<br />
                步驟二：約定評估及取模時間<br />
                步驟三：取模製作<br />
                步驟四：取件試穿並微調<br />
                步驟五：持續追蹤，如有問題可直接到門市處理或收件<br />
                <br /><br />
                <span class="font-blue-14">材質</span>
                <br />
                高溫熱塑 PP 板、EVA抗壓型緩衝墊、可動或不可動關節
                <br /><br />
                <span class="font-blue-14">適用族群</span>
                <br />
                AFO 托足板 ─ 中風、腳盤無力、垂足或是經醫師認定需要者。
                <br />
                SMO 踝上硬式足墊 ─ 強力維持足部軸線，需要支撐但不用像 AFO 固定強度的人。常見特殊小兒垂足或張力偏低者
                <br />
                UCBL ─ 有嚴重扁平足，或是足底結構變形而需要強大支撐者，像是嚴重小兒扁平足
