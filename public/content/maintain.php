            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;這類病人的病情很複雜，實在是「一言難盡」。墊了鞋墊還沒好，也有可能是鞋墊本身沒訂做好，有病人訂做2萬多元的鞋墊還是沒好，並非價格高的鞋墊就品質好。建議S小姐穿寬鬆的鞋子，讓腳趾能在鞋裡跳舞，千萬別穿間窄鞋、高跟鞋、楔形鞋等。<br /><br />
            <div class="dotline"></div><br />
